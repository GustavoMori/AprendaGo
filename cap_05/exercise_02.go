// Escreva expressoes utilizando os seguintes operadores, e atribua seus valores a variaveis.
// ==, <, >, =, <=, >=, !=
package main

import "fmt"

func main() {
	x := 1
	y := 2

	fmt.Println(x == y)
	fmt.Println(x != y)
	fmt.Println(x < y)
	fmt.Println(x > y)
	fmt.Println(x >= y)
	fmt.Println(x <= y)
}
